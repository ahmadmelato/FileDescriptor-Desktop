    public FileItem(FileModel fileModel) {
        initComponents();
        mainViewModel = new MainViewModel();
        this.fileModel = fileModel;

        if (this.fileModel != null) {
            jLabelName.setText(fileModel.file_name);
            jLabelSender.setText(fileModel.extension);
            jLabelSize.setText(fileModel.file_size + " بايت");
            jLabelName.setText(fileModel.file_name);
            jLabelDate.setText(fileModel.getCreateDate());
            jProgressBar1.setValue(0);

        }

        mainViewModel.working.observe((PropertyChangeEvent evt) -> {
            Working working = (Working) evt.getNewValue();
            if (working.isRunning()) {
                cusButton1.setText(working.getsSmg());
            } else if (working.isSuccessful()) {
                cusButton1.setText("فك تشفير");
                readyToOpen = true;
                canOpen = false;
            } else {
                readyToOpen = false;
                canOpen = false;
                cusButton1.setText("تنزيل");
            }
            cusButton1.setEnabled(!working.isRunning());
        });

        mainViewModel.progressValueLiveData.observe((PropertyChangeEvent evt) -> {
            Integer integer = (Integer) evt.getNewValue();
            if (integer != null) {
                jProgressBar1.setValue(integer);
            }
        });
    }

