    private void cusButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cusButton1ActionPerformed
        // TODO add your handling code here:
        if (mainViewModel.working.getValue().isRunning()) {
            return;
        }
        if (!readyToOpen) {
            mainViewModel.downloadFile(this.fileModel);
        } else if (!canOpen) {
            try {
                AESFileEncryptor fileEncryptor = new AESFileEncryptor();
                File file = new File(fileModel.getFileAllName());
                File decryptedFile = new File(file.getName());
                fileEncryptor.decryptFile(this.fileModel.scert_key, file, decryptedFile);
                cusButton1.setText("فتح");
                canOpen = true;
            } catch (Exception ex) {
                System.getLogger(FileItem.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        } else {
            
            if (Desktop.isDesktopSupported()) {
                try {
                    File file = new File(fileModel.getFileAllName());
                    Desktop.getDesktop().open(file);
                    mainViewModel.file_received(fileModel.file_id);
                } catch (IOException ex) {
                    System.err.println(ex.getLocalizedMessage());
                }
            }
        }
    }//GEN-LAST:event_cusButton1ActionPerformed

    
    

