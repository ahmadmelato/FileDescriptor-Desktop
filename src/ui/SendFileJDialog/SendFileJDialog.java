    public SendFileJDialog(javax.swing.JFrame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        this.dialogResult = JOptionPane.DEFAULT_OPTION;

        this.mainViewModel = new MainViewModel();

        mainViewModel.working.observe((PropertyChangeEvent evt) -> {
            Working working = (Working) evt.getNewValue();
            stateParProgress.setBusy(working.isRunning());
            stateParProgress.setVisible(working.isRunning());
            stateParMsg.setText(working.getsSmg());
            stateParMsg.setForeground(working.getColor());
            jButton2.setEnabled(!working.isRunning());
            jButton1.setEnabled(!working.isRunning());
        });

        jPanelUsers.removeAll();
        jPanelUsers.revalidate();
        jPanelUsers.repaint();
        mainViewModel.usersLiveData.observe((PropertyChangeEvent evt) -> {
            List<UserModel> fileModels = (List<UserModel>) evt.getNewValue();
            jPanelUsers.removeAll();
            for (UserModel fileModel : fileModels) {
                JCheckBox cc = new JCheckBox();
                cc.setText(fileModel.user_name + " (" + fileModel.center_name + ")");
                cc.setComponentOrientation(ComponentOrientation.RIGHT_TO_LEFT);
                cc.setFont(new java.awt.Font("DIN Next LT Arabic", 0, 15));
                cc.setMargin(new Insets(5, 10, 5, 10));
                jPanelUsers.add(cc);
            }
            jPanelUsers.revalidate();
            jPanelUsers.repaint();
        });

        mainViewModel.get_all_users();
    }

    public static int showDialog(Component parentComponent) {
        SendFileJDialog d = new SendFileJDialog((javax.swing.JFrame) parentComponent, true);
        d.setVisible(true);
        return d.dialogResult;
    }

