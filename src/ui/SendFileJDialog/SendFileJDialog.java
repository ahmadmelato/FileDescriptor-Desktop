    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        List<Integer> Ids = new ArrayList<>();
        for (int i = 0; i < jPanelUsers.getComponents().length; i++) {
            JCheckBox sBox = (JCheckBox) jPanelUsers.getComponent(i);
            if (sBox.isSelected()) {
                Ids.add(mainViewModel.usersLiveData.getValue().get(i).id);
            }
        }
        if (!jTextFieldPath.getText().isEmpty() && !Ids.isEmpty() && data.JOptionContiuneDialog.showConfirmDialog(this.getParent()) == JOptionPane.YES_OPTION) {
            try {
                AESFileEncryptor fileEncryptor = new AESFileEncryptor();
                // Generate and display a random key
                SecretKey randomKey = generateRandomAESKey();
                String password = Base64.getEncoder().encodeToString(randomKey.getEncoded());
                File selectedFile = new File(jTextFieldPath.getText());
                File encryptedFile = new File(selectedFile.getName());
                fileEncryptor.encryptFile(password, selectedFile, encryptedFile);
                
                mainViewModel.uploadFile(encryptedFile, password, Ids,this);

            } catch (Exception ex) {
                System.err.println("Error: " + ex.getMessage());
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

